// Generator for dictionary/data_gen.go.
//
// Invoked automatically via:
//
//	go generate ./dictionary
//
// Manual invocation from any directory:
//
//	go run ./dictionary/cmd/gen -src ./dictionary -out ./dictionary/data_gen.go
package main

import (
	"bytes"
	"flag"
	"fmt"
	"go/format"
	"math"
	"os"
	"path/filepath"
	"sort"
	"strconv"
	"strings"
	"text/template"

	"gopkg.in/yaml.v3"

	"github.com/borschtapp/kapusta/dictionary"
)

// Template indentation does not need to be perfect — go/format cleans it up.
const tmpl = `// Code generated by go generate; DO NOT EDIT.
package dictionary

var dictMap = map[string]*Dict{
{{range .}}
{{printf "%q" .Lang}}: {
Units: map[string][]string{
{{range .Units}}{{printf "%q" .Key}}: { {{range .Vals}}{{printf "%q" .}}, {{end}} },
{{end}}
},
{{if .TimeUnits}}TimeUnits: map[string][]string{
{{range .TimeUnits}}{{printf "%q" .Key}}: { {{range .Vals}}{{printf "%q" .}}, {{end}} },
{{end}}
},{{end}}
{{if .TemperatureUnits}}TemperatureUnits: map[string][]string{
{{range .TemperatureUnits}}{{printf "%q" .Key}}: { {{range .Vals}}{{printf "%q" .}}, {{end}} },
{{end}}
},{{end}}
{{if .SizeSuffix}}SizeSuffix: []string{ {{range .SizeSuffix}}{{printf "%q" .}}, {{end}} },{{end}}
{{if .QuantityBetween}}QuantityBetween: []string{ {{range .QuantityBetween}}{{printf "%q" .}}, {{end}} },{{end}}
Numbers: map[string]float64{
{{range .Numbers}}{{printf "%q" .Key}}: {{.Val}},
{{end}}
},
},
{{end}}
}

func init() {
for _, d := range dictMap {
d.buildTrie()
}
}
`

type unitEntry struct {
	Key  string
	Vals []string
}

type numberEntry struct {
	Key string
	Val string
}

type dictionaryData struct {
	Lang             string
	Units            []unitEntry
	TimeUnits        []unitEntry
	TemperatureUnits []unitEntry
	SizeSuffix       []string
	QuantityBetween  []string
	Numbers          []numberEntry
}

func sortedUnitEntries(m map[string][]string) []unitEntry {
	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	entries := make([]unitEntry, 0, len(keys))
	for _, k := range keys {
		entries = append(entries, unitEntry{Key: k, Vals: m[k]})
	}
	return entries
}

func sortedNumberEntries(m map[string]float64) []numberEntry {
	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	entries := make([]numberEntry, 0, len(keys))
	for _, k := range keys {
		entries = append(entries, numberEntry{Key: k, Val: formatFloat(m[k])})
	}
	return entries
}

func formatFloat(f float64) string {
	if f == math.Trunc(f) && math.Abs(f) < 1e15 {
		return strconv.FormatInt(int64(f), 10)
	}
	return strconv.FormatFloat(f, 'f', -1, 64)
}

func main() {
	srcFlag := flag.String("src", ".", "directory containing the YAML dictionaries")
	outFlag := flag.String("out", "data_gen.go", "output file path")
	flag.Parse()

	srcDir, err := filepath.Abs(*srcFlag)
	if err != nil {
		panic(err)
	}

	outPath := *outFlag
	if !filepath.IsAbs(outPath) {
		outPath = filepath.Join(srcDir, outPath)
	}

	entries, err := os.ReadDir(srcDir)
	if err != nil {
		panic(err)
	}

	var langs []dictionaryData
	for _, e := range entries {
		if e.IsDir() || !strings.HasSuffix(e.Name(), ".yml") {
			continue
		}
		data, err := os.ReadFile(filepath.Join(srcDir, e.Name()))
		if err != nil {
			panic(err)
		}
		var d dictionary.Dict
		if err := yaml.Unmarshal(data, &d); err != nil {
			panic(err)
		}

		lang := strings.TrimSuffix(e.Name(), ".yml")

		units := sortedUnitEntries(d.Units)
		timeUnits := sortedUnitEntries(d.TimeUnits)
		tempUnits := sortedUnitEntries(d.TemperatureUnits)
		numbers := sortedNumberEntries(d.Numbers)

		langs = append(langs, dictionaryData{
			Lang:             lang,
			Units:            units,
			TimeUnits:        timeUnits,
			TemperatureUnits: tempUnits,
			SizeSuffix:       d.SizeSuffix,
			QuantityBetween:  d.QuantityBetween,
			Numbers:          numbers,
		})
	}

	sort.Slice(langs, func(i, j int) bool { return langs[i].Lang < langs[j].Lang })

	var buf bytes.Buffer
	t := template.Must(template.New("").Parse(tmpl))
	if err := t.Execute(&buf, langs); err != nil {
		panic(err)
	}

	src, err := format.Source(buf.Bytes())
	if err != nil {
		// Write raw output so the caller can inspect what went wrong.
		_ = os.WriteFile(outPath, buf.Bytes(), 0644)
		panic(fmt.Sprintf("go/format failed: %v", err))
	}

	if err := os.WriteFile(outPath, src, 0644); err != nil {
		panic(err)
	}
	fmt.Printf("generated %s\n", outPath)
}
