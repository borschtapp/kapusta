// Generator for dictionary/data_gen.go.
//
// Invoked automatically via:
//
//	go generate ./dictionary
//
// Manual invocation from any directory:
//
//	go run ./dictionary/cmd/gen -src ./dictionary -out ./dictionary/data_gen.go
package main

import (
	"bytes"
	"flag"
	"fmt"
	"go/format"
	"math"
	"os"
	"path/filepath"
	"sort"
	"strconv"
	"strings"
	"text/template"

	"gopkg.in/yaml.v3"
)

type dictYAML struct {
	Units           map[string][]string `yaml:"units"`
	QuantityBetween []string            `yaml:"quantityBetween"`
	Numbers         map[string]float64  `yaml:"numbers"`
}

// Template indentation does not need to be perfect — go/format cleans it up.
const tmpl = `// Code generated by go generate; DO NOT EDIT.
package dictionary

var dictMap = map[string]*Dict{
{{range .}}
{{printf "%q" .Lang}}: {
Units: map[string][]string{
{{range .Units}}{{printf "%q" .Key}}: { {{range .Vals}}{{printf "%q" .}}, {{end}} },
{{end}}
},
{{if .QB}}QuantityBetween: []string{ {{range .QB}}{{printf "%q" .}}, {{end}} },{{end}}
Numbers: map[string]float64{
{{range .Numbers}}{{printf "%q" .Key}}: {{.Val}},
{{end}}
},
},
{{end}}
}

func init() {
for _, d := range dictMap {
d.buildTrie()
}
}
`

type unitEntry struct {
	Key  string
	Vals []string
}

type numberEntry struct {
	Key string
	Val string
}

type langData struct {
	Lang    string
	Units   []unitEntry
	QB      []string
	Numbers []numberEntry
}

func formatFloat(f float64) string {
	if f == math.Trunc(f) && math.Abs(f) < 1e15 {
		return strconv.FormatInt(int64(f), 10)
	}
	return strconv.FormatFloat(f, 'f', -1, 64)
}

func main() {
	srcFlag := flag.String("src", ".", "directory containing the YAML dictionaries")
	outFlag := flag.String("out", "data_gen.go", "output file path")
	flag.Parse()

	srcDir, err := filepath.Abs(*srcFlag)
	if err != nil {
		panic(err)
	}

	outPath := *outFlag
	if !filepath.IsAbs(outPath) {
		outPath = filepath.Join(srcDir, outPath)
	}

	entries, err := os.ReadDir(srcDir)
	if err != nil {
		panic(err)
	}

	var langs []langData
	for _, e := range entries {
		if e.IsDir() || !strings.HasSuffix(e.Name(), ".yml") {
			continue
		}
		data, err := os.ReadFile(filepath.Join(srcDir, e.Name()))
		if err != nil {
			panic(err)
		}
		var d dictYAML
		if err := yaml.Unmarshal(data, &d); err != nil {
			panic(err)
		}

		lang := strings.TrimSuffix(e.Name(), ".yml")

		unitKeys := make([]string, 0, len(d.Units))
		for k := range d.Units {
			unitKeys = append(unitKeys, k)
		}
		sort.Strings(unitKeys)
		units := make([]unitEntry, 0, len(unitKeys))
		for _, k := range unitKeys {
			units = append(units, unitEntry{Key: k, Vals: d.Units[k]})
		}

		numKeys := make([]string, 0, len(d.Numbers))
		for k := range d.Numbers {
			numKeys = append(numKeys, k)
		}
		sort.Strings(numKeys)
		numbers := make([]numberEntry, 0, len(numKeys))
		for _, k := range numKeys {
			numbers = append(numbers, numberEntry{Key: k, Val: formatFloat(d.Numbers[k])})
		}

		langs = append(langs, langData{
			Lang:    lang,
			Units:   units,
			QB:      d.QuantityBetween, // nil if omitted in YAML — template guards with {{if .QB}}
			Numbers: numbers,
		})
	}

	sort.Slice(langs, func(i, j int) bool { return langs[i].Lang < langs[j].Lang })

	var buf bytes.Buffer
	t := template.Must(template.New("").Parse(tmpl))
	if err := t.Execute(&buf, langs); err != nil {
		panic(err)
	}

	src, err := format.Source(buf.Bytes())
	if err != nil {
		// Write raw output so the caller can inspect what went wrong.
		_ = os.WriteFile(outPath, buf.Bytes(), 0644)
		panic(fmt.Sprintf("go/format failed: %v", err))
	}

	if err := os.WriteFile(outPath, src, 0644); err != nil {
		panic(err)
	}
	fmt.Printf("generated %s\n", outPath)
}
